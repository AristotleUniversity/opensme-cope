    private void addDependencyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDependencyButtonActionPerformed
        JFileChooser newReuseProjectJarFileChooser = new JFileChooser();
        newReuseProjectJarFileChooser.setCurrentDirectory(new File(lastDirectory));
        FileFilter filter = new FileNameExtensionFilter("JAR file", "jar");
        newReuseProjectJarFileChooser.addChoosableFileFilter(filter);
        newReuseProjectJarFileChooser.setMultiSelectionEnabled(true);
        int returnValue = newReuseProjectJarFileChooser.showOpenDialog(this);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File[] files = newReuseProjectJarFileChooser.getSelectedFiles();
            DefaultListModel model = (DefaultListModel) dependenciesList.getModel();
            for (File file : files) {
                model.addElement(file.getAbsolutePath());
                if(!lastDirectory.equals(file.getParent())){
                    lastDirectory = file.getParent();
                }
            }
        }
    }//GEN-LAST:event_addDependencyButtonActionPerformed

    private void removeDependencyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeDependencyButtonActionPerformed
        DefaultListModel model = (DefaultListModel) dependenciesList.getModel();
        int[] selectedIndices = dependenciesList.getSelectedIndices();
        for (int i = selectedIndices.length-1; i>=0; i--) {
            model.remove(i);
        }
    }//GEN-LAST:event_removeDependencyButtonActionPerformed

    private void srcDirChooseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_srcDirChooseButtonActionPerformed
        JFileChooser srcDirFileChooser = new JFileChooser();
        srcDirFileChooser.setCurrentDirectory(new File(lastDirectory));
        srcDirFileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        srcDirFileChooser.setMultiSelectionEnabled(false);
        int returnValue = srcDirFileChooser.showOpenDialog(this);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File file = srcDirFileChooser.getSelectedFile();
            lastDirectory = file.getAbsolutePath();
            srcDirTextField.setText(file.getAbsolutePath());
        }
    }//GEN-LAST:event_srcDirChooseButtonActionPerformed

