    private void viewSourceMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewSourceMenuItemActionPerformed
        if (selectedClass != null) {
            String text = null;
            String filePath = this.reuseProject.getSrcDir()+ File.separator + selectedClass.getName().replace('.', File.separatorChar) + ".java";
            File javaFile = new File(filePath);
            JDialog sourceDialog = new JDialog();
            JPanel cp = new JPanel(new BorderLayout());
            try {
                text = getSourceForFile(javaFile);
            } catch (FileNotFoundException ex) {
                cp.add(new JLabel("File not found: " + filePath));
                sourceDialog.setContentPane(cp);
                sourceDialog.setEnabled(true);
                sourceDialog.pack();
                return;
            }
            RSyntaxTextArea textArea = new RSyntaxTextArea();
            textArea.setSyntaxEditingStyle(SyntaxConstants.SYNTAX_STYLE_JAVA);
            textArea.setCodeFoldingEnabled(true);
            textArea.setAntiAliasingEnabled(true);
            textArea.setEditable(false);
            
            textArea.setText(text);
            RTextScrollPane sp = new RTextScrollPane(textArea);
            sp.setFoldIndicatorEnabled(true);
            cp.add(sp);

            sourceDialog.setContentPane(cp);
            sourceDialog.setTitle(javaFile.getName() + " (" + selectedClass.getName() +")");
            sourceDialog.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
            sourceDialog.setVisible(true);
            sourceDialog.pack();
            sourceDialog.setLocationRelativeTo(null);
        }
    }//GEN-LAST:event_viewSourceMenuItemActionPerformed

    private String getSourceForFile(File javaFile) throws FileNotFoundException {
        StringBuilder text = new StringBuilder();
        String NL = System.getProperty("line.separator");
        Scanner scanner = new Scanner(new FileInputStream(javaFile));
        try {
            while (scanner.hasNextLine()) {
                text.append(scanner.nextLine() + NL);
            }
        } finally {
            scanner.close();
        }
        return text.toString();
    }

    public final void listAllFiles(File directory, DefaultMutableTreeNode parent, Boolean recursive) {
        File[] children = directory.listFiles(); // list all the files in the directory
        for (File child : children) { // loop through each
            DefaultMutableTreeNode node = new DefaultMutableTreeNode(new FileInfo(child));
            // only display the node if it isn't a folder, and if this is a recursive call
            if (child.isDirectory() && recursive) {
                parent.add(node); // add as a child node
                listAllFiles(child, node, recursive); // call again for the subdirectory
            } else if (!child.isDirectory()) { // otherwise, if it isn't a directory
                parent.add(node); // add it as a node and do nothing else
            }
        }
    }

    private void initColumnSizes(javax.swing.JTable table) {
        ClassMetricsTableModel model = (ClassMetricsTableModel) table.getModel();
        javax.swing.table.TableColumn column = null;
        Component comp = null;
        int headerWidth = 0;
        int cellWidth = 0;
        Object[] exampleValues = model.exampleValues();
        javax.swing.table.TableCellRenderer headerRenderer =
                table.getTableHeader().getDefaultRenderer();
        int columnCount = model.getColumnCount();
        for (int i = 0; i < columnCount; i++) {
            column = table.getColumnModel().getColumn(i);

            comp = headerRenderer.getTableCellRendererComponent(
                    null, column.getHeaderValue(),
                    false, false, 0, 0);
            headerWidth = comp.getPreferredSize().width;

            comp = table.getDefaultRenderer(model.getColumnClass(i)).
                    getTableCellRendererComponent(
                    table, exampleValues[i],
                    false, false, 0, i);
            cellWidth = comp.getPreferredSize().width;


            System.out.println("Initializing width of column "
                    + i + ". "
                    + "headerWidth = " + headerWidth
                    + "; cellWidth = " + cellWidth);


            column.setPreferredWidth(Math.max(headerWidth, cellWidth));
        }
    }

    private class CustomTableCellRenderer extends DefaultTableCellRenderer {

        int threshold = 0;

        public CustomTableCellRenderer(int threshold) {
            this.threshold = threshold;
        }

        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected,
                boolean hasFocus, int row, int column) {

            Component cell = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            if (row < threshold) {
                cell.setBackground(Color.pink);
            } else {
                cell.setBackground(new Color(242, 241, 240));
            }

            return cell;
        }
    }
