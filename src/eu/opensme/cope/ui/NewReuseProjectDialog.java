    private void fileChooserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fileChooserButtonActionPerformed
        JFileChooser newReuseProjectJarFileChooser = new JFileChooser();
        newReuseProjectJarFileChooser.setCurrentDirectory(new File(lastDirectory));
        FileFilter filter = new FileNameExtensionFilter("JAR file", "jar");
        newReuseProjectJarFileChooser.addChoosableFileFilter(filter);
        int returnValue = newReuseProjectJarFileChooser.showOpenDialog(this);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File file = newReuseProjectJarFileChooser.getSelectedFile();
            lastDirectory = file.getParent();
            jarFilenameTextField.setText(file.getAbsolutePath());
        }
    }//GEN-LAST:event_fileChooserButtonActionPerformed

    private void createNewReuseProjectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createNewReuseProjectButtonActionPerformed
        creationProblemLabel.setText("");
        String projectName = reuseProjectNameTextField.getText();
        if ("".equals(projectName) || projectName == null) {
            creationProblemLabel.setText("You must provide a project name");
            return;
        }
        String projectLocation = ReuseProject.getReuseProjectsLocation() + projectName;

        String projectJARFilename = jarFilenameTextField.getText();
        if ("".equals(projectJARFilename) || projectJARFilename == null) {
            creationProblemLabel.setText("You must provide a project jar file");
            return;
        }

        List<String> dependenciesJARs = new ArrayList<String>();
        int listSize = dependenciesList.getModel().getSize();
        for (int i=0; i<listSize; i++) {
            String s = (String) dependenciesList.getModel().getElementAt(i);
            dependenciesJARs.add(s);
        }

        String repoURL = repoURLTextField.getText();

        String srcDir = srcDirTextField.getText();
        if ("".equals(srcDir) || srcDir == null) {
            creationProblemLabel.setText("You must provide a source code directory");
            return;
        }

        ReuseProject reuseProject = new ReuseProject(projectLocation, 
                projectName,
                projectJARFilename,
                repoURL,
                dependenciesJARs,
                srcDir);

        try {
            boolean success = reuseProject.createReuseProject();
            if (!success) {
                creationProblemLabel.setText("Project directory already exists. Please choose another.");
                return;
            }
        } catch (IOException ioe) {
            creationProblemLabel.setText(ioe.getMessage());
            return;
        } catch (HibernateException he) {
            creationProblemLabel.setText(he.getMessage());
            return;
        }
        this.project = reuseProject;
        this.setVisible(false);
    }//GEN-LAST:event_createNewReuseProjectButtonActionPerformed

