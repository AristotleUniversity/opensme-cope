    public void visualizeSearchResults(Vector<ClassAnalysis> searchResults) {
        if (this.reuseProject == null) {
            return;
        }
        ClassMetricsTableModel model = new ClassMetricsTableModel(reuseProject.getProject());
        int counter = 0;
        for (int i = 0; i < searchResults.size(); i++) {
            int place = model.getPlace(searchResults.elementAt(i));
            if (place < 0) {
                continue;
            }
            ClassAnalysis temp = model.getRowObject(place);
            model.setValue(counter, searchResults.elementAt(i));
            model.setValue(place, temp);
            counter++;
        }
        classMetricsTable.setModel(model);
        this.initColumnSizes(classMetricsTable);
        classMetricsTable.getRowSorter().addRowSorterListener(new RowSorterListener() {

            public void sorterChanged(RowSorterEvent rse) {
                TableCellRenderer renderer = new CustomTableCellRenderer(0);
                classMetricsTable.setDefaultRenderer(Object.class, renderer);
                classMetricsTable.validate();
                repaint();
            }
        });
        TableCellRenderer renderer = new CustomTableCellRenderer(counter);
        classMetricsTable.setDefaultRenderer(Object.class, renderer);
        classMetricsTable.validate();
        this.repaint();
    }

    public void resetSearchResults() {
        TableCellRenderer renderer = new CustomTableCellRenderer(0);
        classMetricsTable.setDefaultRenderer(Object.class, renderer);
        classMetricsTable.validate();
        this.repaint();
    }

